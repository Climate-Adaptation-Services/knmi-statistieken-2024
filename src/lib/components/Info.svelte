      Voor de KNMI’23-klimaatscenario’s zijn tijdreeksen voor de toekomst gemaakt waarmee de effecten van klimaatverandering berekend kunnen worden. Er zijn twee soorten tijdreeksen: getransformeerde tijdreeksen en modeluitvoer. Beide typen datasets staan op het <a target='_blank' href='https://klimaatscenarios-data.knmi.nl/'>dataportaal</a>. De indicatoren in de Klimaateffectatlas zijn berekend met de modeluitvoer. Door een klimaatmodel meerdere keren het klimaat te laten simuleren, kunnen we veel langere tijdreeksen krijgen (in dit geval 240 jaar) die het huidige klimaat (1991-2020) beschrijven. Met behulp van de 30 jaar waarnemingen voor 1991-2020 is deze modeluitvoer gecorrigeerd, zodat de statistische eigenschappen (gemiddelde, eens in de 10 jaar gebeurtenissen, etc.) overeenkomen met de waarnemingen. Dezelfde soort correctie is uitgevoerd voor de modeluitvoer voor het klimaat rond 2050 en 2100. De modeluitvoer is gegeven op een rooster dat Nederland bedekt met roostercellen die ongeveer 12 bij 12 kilometer groot zijn. Op de modeluitvoer is een biascorrectie uitgevoerd zodat de modeluitvoer in de referentieperiode zoveel mogelijk overeenkomt met de observaties. Deze biascorrectie garandeert alleen niet dat de berekende indicatoren voor de referentieperiode exact overeenkomen met de waarnemingen van die indicatoren in de referentieperiode zoals weergegeven op de website van het KNMI. Dit betekent dat de kaarten die je hier bij “referentie” ziet, enigszins kunnen verschillen van de kaarten in de Klimaatviewer van het <a target='_blank' href='https://www.knmi.nl/kennis-en-datacentrum/achtergrond/modeluitvoer-knmi-23-klimaatscenario-s'>KNMI</a>.   
