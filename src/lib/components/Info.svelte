    <h3>Berekening scenario’s</h3>
    <p>
      Per tijdshorizon zijn er vier KNMI’23-klimaatscenario’s: Ld (Laag droog), Ln (Laag nat), Hd (Hoog droog) en Hn (Hoog nat). De Klimaateffectatlas toont per indicator alleen een hoogste en een laagste scenario. Dit zijn de scenario’s met respectievelijk de hoogste en de laagste verandering ten opzichte van het huidige klimaat.  
    </p>
    <h3>Berekening variabiliteit</h3>
    <p>
      De modeluitvoer bevat per scenario 8 sets van 30 jaar aan data. In totaal gaat het dus om 240 jaar aan data per scenario. De 90-procent band, weergegeven als de jaar-op-jaar variatie, is het 5e en het 95e percentiel van die 240 jaar aan data.  
    </p>
    <h3>Landelijke dekking</h3>
    <p>
