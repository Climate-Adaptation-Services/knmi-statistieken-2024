    <h2>Explanation of KNMI Scenarios</h2>
      <h3>In Brief</h3>
      <p>
        The climate statistics on this page present the KNMI'23 climate scenarios for temperature, precipitation, and sea level rise. The data is provided by KNMI and the visualizations are created by the Climate Adaptation Services (CAS).
      </p>
      <h3>Data Used</h3>
      <p>
        For the KNMI'23 climate scenarios, future time series have been created to calculate the effects of climate change. There are two types of time series: transformed time series and model outputs. Both types of datasets are available on the <a target='_blank' href='https://klimaatscenarios-data.knmi.nl/'>data portal</a>. The indicators in the Climate Impact Atlas are calculated using the model outputs. By running a climate model multiple times, we can obtain much longer time series (in this case, 240 years) that describe the current climate (1991-2020). Using the 30 years of observations for 1991-2020, this model output has been corrected so that the statistical properties (average, events occurring once in 10 years, etc.) match the observations. The same type of correction has been applied to the model output for the climate around 2050 and 2100. The model output is provided on a grid covering the Netherlands with grid cells approximately 12 by 12 kilometers in size. A bias correction has been applied to ensure that the model output for the reference period aligns as closely as possible with the observations. However, this bias correction does not guarantee that the calculated indicators for the reference period will match exactly with the observations of those indicators in the reference period as displayed on the KNMI website. This means that the maps you see here under "reference" may differ slightly from the maps in the <a target='_blank' href='https://www.knmi.nl/klimaat-viewer'>KNMI Climate Viewer</a>.
      </p>
      <br>
      <p>On the KNMI website, you can find more <a target='_blank' href='https://www.knmi.nl/kennis-en-datacentrum/achtergrond/modeluitvoer-knmi-23-klimaatscenario-s'>detailed information about the model outputs</a>. KNMI also provides <a target='_blank' href='https://www.knmi.nl/kennis-en-datacentrum/achtergrond/knmi-23-klimaatscenario-s'>general information about the KNMI'23 climate scenarios</a> on its website.</p>
      <h3>Scenario Calculation</h3>
      <p>
        For each time horizon, there are four KNMI'23 climate scenarios: Ld (Low dry), Ln (Low wet), Hd (High dry), and Hn (High wet). The Climate Impact Atlas displays only the highest and lowest scenarios for each indicator. These are the scenarios with the highest and lowest changes relative to the current climate.
      </p>
      <h3>Calculation of Variability</h3>
      <p>
        The model output contains 8 sets of 30 years of data per scenario. In total, this amounts to 240 years of data per scenario. The 90-percent band, shown as year-to-year variation, is the 5th and 95th percentiles of that 240 years of data.
      </p>
      <h3>National Coverage</h3>
      <p>
        Along the coast and major water bodies in Zeeland, there are fewer data points available. Choose the nearest point to the location you are interested in. The climate model calculates the climate for each grid of approximately 12 by 12 km. If such a grid contains a lot of water, the model treats the entire grid as water. This makes the information for that grid less reliable. Therefore, grids with a lot of water have not been included in the maps. Along the national borders, only those grids that cover more than 50% of the Netherlands have been included.
      </p>
